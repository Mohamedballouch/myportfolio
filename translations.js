// Translation utility functions
class TranslationManager {
    constructor() {
        this.currentLang = this.getSavedLanguage() || 'en';
        this.translations = translations;
    }
    
    getSavedLanguage() {
        return localStorage.getItem('portfolio-language');
    }
    
    saveLanguage(lang) {
        localStorage.setItem('portfolio-language', lang);
    }
    
    setLanguage(lang) {
        if (this.translations[lang]) {
            this.currentLang = lang;
            this.saveLanguage(lang);
            this.updateContent();
            this.updateLanguageToggle();
            this.updateHtmlLang();
        }
    }
    
    getCurrentLanguage() {
        return this.currentLang;
    }
    
    translate(key) {
        const keys = key.split('.');
        let result = this.translations[this.currentLang];
        
        for (const k of keys) {
            result = result?.[k];
        }
        
        return result || key;
    }
    
    updateContent() {
        // Update meta tags
        this.updateMetaTags();
        
        // Update navigation
        this.updateNavigation();
        
        // Update hero section
        this.updateHeroSection();
        
        // Update about section
        this.updateAboutSection();
        
        // Update skills section
        this.updateSkillsSection();
        
        // Update experience section
        this.updateExperienceSection();
        
        // Update projects section
        this.updateProjectsSection();
        
        // Update publications section
        this.updatePublicationsSection();
        
        // Update education section
        this.updateEducationSection();
        
        // Update contact section
        this.updateContactSection();
        
        // Update footer
        this.updateFooter();
    }
    
    updateMetaTags() {
        document.title = this.translate('meta.title');
        
        const metaDescription = document.querySelector('meta[name="description"]');
        if (metaDescription) {
            metaDescription.setAttribute('content', this.translate('meta.description'));
        }
        
        const metaKeywords = document.querySelector('meta[name="keywords"]');
        if (metaKeywords) {
            metaKeywords.setAttribute('content', this.translate('meta.keywords'));
        }
        
        const ogTitle = document.querySelector('meta[property="og:title"]');
        if (ogTitle) {
            ogTitle.setAttribute('content', this.translate('meta.ogTitle'));
        }
        
        const ogDescription = document.querySelector('meta[property="og:description"]');
        if (ogDescription) {
            ogDescription.setAttribute('content', this.translate('meta.ogDescription'));
        }
    }
    
    updateNavigation() {
        const navLinks = document.querySelectorAll('.nav-links a');
        const navKeys = ['home', 'about', 'skills', 'experience', 'projects', 'publications', 'education', 'contact'];
        
        navLinks.forEach((link, index) => {
            if (navKeys[index]) {
                link.textContent = this.translate(`nav.${navKeys[index]}`);
            }
        });
    }
    
    updateHeroSection() {
        const heroSubtitle = document.querySelector('.hero-subtitle');
        const heroDescription = document.querySelector('.hero-description');
        const getInTouchBtn = document.querySelector('.btn-primary');
        const viewProjectsBtn = document.querySelector('.btn-secondary');
        
        if (heroSubtitle) heroSubtitle.textContent = this.translate('hero.subtitle');
        if (heroDescription) heroDescription.textContent = this.translate('hero.description');
        if (getInTouchBtn) {
            getInTouchBtn.innerHTML = `<i class="fas fa-envelope"></i> ${this.translate('hero.getInTouch')}`;
        }
        if (viewProjectsBtn) {
            viewProjectsBtn.innerHTML = `<i class="fas fa-code"></i> ${this.translate('hero.viewProjects')}`;
        }
    }
    
    updateAboutSection() {
        const aboutTitle = document.querySelector('#about .section-title');
        const aboutSubtitle = document.querySelector('.about-text h3');
        const aboutParagraphs = document.querySelectorAll('.about-text p');
        const statLabels = document.querySelectorAll('.stat-label');
        
        if (aboutTitle) aboutTitle.textContent = this.translate('about.title');
        if (aboutSubtitle) aboutSubtitle.textContent = this.translate('about.subtitle');
        
        if (aboutParagraphs.length >= 2) {
            aboutParagraphs[0].textContent = this.translate('about.description1');
            aboutParagraphs[1].textContent = this.translate('about.description2');
        }
        
        const statKeys = ['experience', 'projects', 'publications'];
        statLabels.forEach((label, index) => {
            if (statKeys[index]) {
                label.textContent = this.translate(`about.stats.${statKeys[index]}`);
            }
        });
    }
    
    updateSkillsSection() {
        const skillsTitle = document.querySelector('#skills .section-title');
        const skillCategories = document.querySelectorAll('.skill-category h3');
        
        if (skillsTitle) skillsTitle.textContent = this.translate('skills.title');
        
        const categoryKeys = ['aiml', 'llms', 'cloud', 'database'];
        skillCategories.forEach((category, index) => {
            if (categoryKeys[index]) {
                const icon = category.querySelector('i');
                const iconHTML = icon ? icon.outerHTML : '';
                category.innerHTML = `${iconHTML} ${this.translate(`skills.categories.${categoryKeys[index]}`)}`;
            }
        });
    }
    
    updateExperienceSection() {
        const experienceTitle = document.querySelector('#experience .section-title');
        if (experienceTitle) experienceTitle.textContent = this.translate('experience.title');
        
        const timelineItems = document.querySelectorAll('.timeline-item');
        const positionKeys = ['current', 'senior', 'dataScientist'];
        
        timelineItems.forEach((item, index) => {
            if (positionKeys[index]) {
                const positionKey = positionKeys[index];
                const titleElement = item.querySelector('.timeline-content h3');
                const companyElement = item.querySelector('.timeline-company span');
                const dateElement = item.querySelector('.timeline-date');
                const achievements = item.querySelectorAll('.timeline-description li');
                
                if (titleElement) titleElement.textContent = this.translate(`experience.positions.${positionKey}.title`);
                if (companyElement) companyElement.textContent = this.translate(`experience.positions.${positionKey}.companyName`);
                if (dateElement) dateElement.textContent = this.translate(`experience.positions.${positionKey}.date`);
                
                const achievementTexts = this.translate(`experience.positions.${positionKey}.achievements`);
                achievements.forEach((achievement, achIndex) => {
                    if (achievementTexts[achIndex]) {
                        achievement.textContent = achievementTexts[achIndex];
                    }
                });
            }
        });
    }
    
    updateProjectsSection() {
        const projectsTitle = document.querySelector('#projects .section-title');
        if (projectsTitle) projectsTitle.textContent = this.translate('projects.title');
        
        const projectCards = document.querySelectorAll('.project-card');
        const projectKeys = ['multimodal', 'churn', 'social', 'bertopic'];
        
        projectCards.forEach((card, index) => {
            if (projectKeys[index]) {
                const projectKey = projectKeys[index];
                const titleElement = card.querySelector('.project-content h3');
                const descriptionElement = card.querySelector('.project-content p');
                const viewCodeLink = card.querySelector('.project-links a');
                
                if (titleElement) titleElement.textContent = this.translate(`projects.items.${projectKey}.title`);
                if (descriptionElement) descriptionElement.textContent = this.translate(`projects.items.${projectKey}.description`);
                if (viewCodeLink) {
                    viewCodeLink.innerHTML = `<i class="fab fa-github"></i> ${this.translate(`projects.items.${projectKey}.viewCode`)}`;
                }
            }
        });
    }
    
    updatePublicationsSection() {
        const publicationsTitle = document.querySelector('#publications .section-title');
        if (publicationsTitle) publicationsTitle.textContent = this.translate('publications.title');
        
        const publicationItems = document.querySelectorAll('.publication-item');
        const publicationKeys = ['reinforcement', 'callCenter'];
        
        publicationItems.forEach((item, index) => {
            if (publicationKeys[index]) {
                const publicationKey = publicationKeys[index];
                const titleElement = item.querySelector('.publication-title');
                const detailsElement = item.querySelector('.publication-details');
                const linkElement = item.querySelector('.publication-link');
                
                if (titleElement) titleElement.textContent = this.translate(`publications.items.${publicationKey}.title`);
                if (detailsElement) detailsElement.textContent = this.translate(`publications.items.${publicationKey}.details`);
                if (linkElement) {
                    linkElement.innerHTML = `<i class="fas fa-external-link-alt"></i> ${this.translate(`publications.items.${publicationKey}.viewPaper`)}`;
                }
            }
        });
    }
    
    updateEducationSection() {
        const educationTitle = document.querySelector('#education .section-title');
        if (educationTitle) educationTitle.textContent = this.translate('education.title');
        
        const educationCards = document.querySelectorAll('.education-card');
        const educationKeys = ['phd', 'engineering', 'exchange'];
        
        educationCards.forEach((card, index) => {
            if (educationKeys[index]) {
                const educationKey = educationKeys[index];
                const degreeElement = card.querySelector('h3');
                const fieldElement = card.querySelector('h4');
                const institutionElement = card.querySelector('p:first-of-type');
                const dateElement = card.querySelector('.education-date');
                
                if (degreeElement) degreeElement.textContent = this.translate(`education.items.${educationKey}.degree`);
                if (fieldElement) fieldElement.textContent = this.translate(`education.items.${educationKey}.field`);
                if (institutionElement) institutionElement.textContent = this.translate(`education.items.${educationKey}.institution`);
                if (dateElement) dateElement.textContent = this.translate(`education.items.${educationKey}.date`);
            }
        });
    }
    
    updateContactSection() {
        const contactTitle = document.querySelector('#contact .section-title');
        const contactDescription = document.querySelector('#contact .contact-content p');
        const locationSpan = document.querySelector('.contact-item span');
        const sendMessageBtn = document.querySelector('#contact .btn-primary');
        
        if (contactTitle) contactTitle.textContent = this.translate('contact.title');
        if (contactDescription) contactDescription.textContent = this.translate('contact.description');
        if (locationSpan) locationSpan.textContent = this.translate('contact.location');
        if (sendMessageBtn) {
            sendMessageBtn.innerHTML = `<i class="fas fa-paper-plane"></i> ${this.translate('contact.sendMessage')}`;
        }
    }
    
    updateFooter() {
        const footerText = document.querySelector('footer p');
        if (footerText) footerText.textContent = this.translate('footer.copyright');
    }
    
    updateLanguageToggle() {
        const toggleBtn = document.querySelector('.language-toggle');
        if (toggleBtn) {
            toggleBtn.classList.toggle('active', this.currentLang === 'fr');
        }
    }
    
    updateHtmlLang() {
        document.documentElement.setAttribute('lang', this.currentLang);
    }
}

// Initialize translation manager
const translationManager = new TranslationManager();

// Export for use in other files
window.translationManager = translationManager;
